# app.py
import os
import gdown
import streamlit as st
import pandas as pd
import numpy as np
from catboost import CatBoostClassifier

# ============================================================
# СТРАНИЦА — ДОЛЖНА БЫТЬ ПЕРВОЙ!
# ============================================================
st.set_page_config(
    page_title='NKO Funding Predictor',
    page_icon='📊',
    layout='centered'
)

# ============================================================
# КОНФИГУРАЦИЯ
# ============================================================
MODEL_PATH = 'nko_funding_model_v3_final.cbm'
THRESHOLD  = 0.750

GDRIVE_FILES = {
    'nko_funding_model_v3_final.cbm': '1gPZKgeTwZhRssAzKUOZKWxMHpJcIve1F',
    'nko_all.parquet':                '1pA6VX8Ee3r0CUELnaR7TbDhZaaLs2DzQ',
    'nko_enriched.parquet':           '1DEQnc4-CBb_ECFWqXmb6fxjpqHSxfL3s',
}

# ============================================================
# АВТОЗАГРУЗКА ФАЙЛОВ С GOOGLE DRIVE
# ============================================================
def download_files():
    for filename, file_id in GDRIVE_FILES.items():
        if not os.path.exists(filename):
            st.info(f'Загружаю {filename} с Google Drive...')
            url = f'https://drive.google.com/uc?id={file_id}'
            gdown.download(url, filename, quiet=False, fuzzy=True)

download_files()

# ============================================================
# ЗАГРУЗКА МОДЕЛИ
# ============================================================
@st.cache_resource
def load_model():
    model = CatBoostClassifier()
    model.load_model(MODEL_PATH)
    return model

model = load_model()

# ============================================================
# СПРАВОЧНИКИ
# ============================================================
REGIONS = sorted([
    'Москва', 'Санкт-Петербург', 'Московская область',
    'Краснодарский край', 'Республика Татарстан',
    'Свердловская область', 'Новосибирская область',
    'Ростовская область', 'Республика Башкортостан',
    'Нижегородская область', 'Самарская область',
    'Челябинская область', 'Красноярский край',
    'Иркутская область', 'Воронежская область',
    'Пермский край', 'Волгоградская область',
    'Саратовская область', 'Омская область',
    'Республика Тыва', 'Чеченская Республика',
    'Ханты-Мансийский АО', 'Ямало-Ненецкий АО',
    'Республика Дагестан', 'Приморский край',
    'Другой регион'
])

OKVED = sorted([
    'Образование',
    'Социальное обслуживание без обеспечения проживания',
    'Деятельность общественных организаций',
    'Деятельность в области культуры, искусства и развлечений',
    'Деятельность спортивных объектов',
    'Деятельность религиозных организаций',
    'Деятельность профессиональных членских организаций',
    'Научные исследования и разработки',
    'Деятельность в области здравоохранения',
    'Предоставление прочих персональных услуг',
    'Другой вид деятельности'
])

OPF = sorted([
    'Фонд',
    'Автономная некоммерческая организация',
    'Общественная организация',
    'Религиозные организации',
    'Ассоциации и союзы',
    'Частное учреждение',
    'Другая форма'
])

# ============================================================
# ЗАГОЛОВОК
# ============================================================
st.title('Прогноз получения финансирования НКО')
st.markdown(
    'Модель оценивает вероятность того, что некоммерческая организация '
    'получит государственное финансирование — гранты или госконтракты. '
    'Используются данные о ~520 тыс. НКО России.'
)
st.markdown('---')

# ============================================================
# ФОРМА ВВОДА
# ============================================================
col1, col2 = st.columns(2)

with col1:
    region   = st.selectbox('Регион регистрации', REGIONS)
    okved    = st.selectbox('Вид деятельности (ОКВЭД)', OKVED)

with col2:
    opf      = st.selectbox('Организационно-правовая форма', OPF)
    reg_year = st.number_input(
        'Год регистрации',
        min_value=1990,
        max_value=2023,
        value=2010,
        step=1
    )

org_age = 2024 - int(reg_year)
st.markdown(f'**Возраст организации:** {org_age} лет')

# ============================================================
# ПРЕДСКАЗАНИЕ
# ============================================================
st.markdown('---')

if st.button('Рассчитать вероятность', type='primary'):

    input_df = pd.DataFrame([{
        'region_name': region,
        'org_age'    : org_age,
        'okved_name' : okved,
        'opf_name'   : opf
    }])

    proba = model.predict_proba(input_df)[0, 1]
    pred  = int(proba >= THRESHOLD)

    st.markdown('### Результат')

    col_res1, col_res2 = st.columns(2)

    with col_res1:
        st.metric(
            label='Вероятность финансирования',
            value=f'{proba:.1%}'
        )

    with col_res2:
        if pred == 1:
            st.success('Вероятно получит финансирование')
        else:
            st.error('Вероятно не получит финансирование')

    st.markdown('**Уровень уверенности модели:**')
    st.progress(float(proba))

    st.markdown('---')
    st.markdown('### Что это значит')

    if proba >= 0.85:
        st.markdown(
            'Организация имеет профиль, характерный для большинства '
            'получателей государственного финансирования в этом регионе '
            'и виде деятельности.'
        )
    elif proba >= THRESHOLD:
        st.markdown(
            'Профиль организации близок к типичным получателям финансирования, '
            'но вероятность не высокая. Конкретный результат зависит от '
            'деталей, которые модель не учитывает: опыта команды, '
            'качества заявки, конкуренции в конкурсе.'
        )
    elif proba >= 0.4:
        st.markdown(
            'Профиль организации не типичен для получателей финансирования '
            'в данном сегменте, но отдельные параметры соответствуют. '
            'Шансы есть, но ниже среднего по выборке.'
        )
    else:
        st.markdown(
            'Организации с таким профилем редко получают государственное '
            'финансирование по историческим данным. '
            'Это не означает невозможность — модель отражает '
            'исторические паттерны, а не формальные требования конкурсов.'
        )

    with st.expander('Технические детали'):
        st.markdown(f"""
        | Параметр | Значение |
        |----------|----------|
        | Модель | CatBoostClassifier v3 |
        | ROC-AUC | 0.866 |
        | PR-AUC | 0.318 |
        | F1 | 0.359 |
        | Порог | {THRESHOLD} |
        | Вероятность (raw) | {proba:.4f} |
        | Обучающая выборка | ~520 тыс. НКО |
        | Дата обучения | 20.07.2026 |
        """)
        st.markdown(
            '**Ограничение:** модель обучена на исторических данных 1990–2021. '
            'Признаки, известные после получения финансирования '
            '(уровень активности, категория дохода), исключены во избежание '
            'утечки данных.'
        )

# ============================================================
# ПОДВАЛ
# ============================================================
st.markdown('---')
st.markdown(
    '<div style="text-align:center; color:gray; font-size:12px;">'
    'NKO Funding Predictor · CatBoost v3 · ROC-AUC 0.866 · '
    'Данные: реестр НКО Минюста, bus.gov.ru'
    '</div>',
    unsafe_allow_html=True
)